import fs from "node:fs";
import path from "node:path";

import matter from "gray-matter";

const rootDir = process.cwd();
const contentDir = path.join(rootDir, "content", "blog");
const outputDir = path.join(rootDir, "lib", "generated");
const outputFile = path.join(outputDir, "blog.generated.ts");
const defaultAccent = "from-sky-400/30 via-cyan-300/10 to-transparent";

function getCurrentDateString() {
  return new Date().toISOString().slice(0, 10);
}

function resolvePublishedAt(value) {
  if (value instanceof Date) {
    return value.toISOString().slice(0, 10);
  }

  if (typeof value === "string") {
    const normalized = value.trim().toLowerCase();

    if (normalized === "today" || normalized === "now" || normalized === "current") {
      return getCurrentDateString();
    }

    if (normalized) {
      return value.trim();
    }
  }

  return getCurrentDateString();
}

function toNonEmptyString(value) {
  if (typeof value !== "string") {
    return null;
  }

  const trimmed = value.trim();
  return trimmed || null;
}

function getMarkdownTitle(content) {
  const match = content.match(/^#\s+(.+)$/m);
  return match?.[1]?.trim() || null;
}

function stripMarkdown(value) {
  return value
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/`[^`]*`/g, " ")
    .replace(/!\[[^\]]*\]\([^)]*\)/g, " ")
    .replace(/\[[^\]]*\]\([^)]*\)/g, " ")
    .replace(/^>\s?/gm, "")
    .replace(/^[-*+]\s+/gm, "")
    .replace(/^\d+\.\s+/gm, "")
    .replace(/^#{1,6}\s+/gm, "")
    .replace(/[*_~]/g, "")
    .trim();
}

function getExcerptFromContent(content) {
  const paragraphs = content
    .split(/\n\s*\n/)
    .map((block) => block.trim())
    .filter(Boolean)
    .filter((block) => !block.startsWith("#"))
    .filter((block) => !block.startsWith("!"))
    .map(stripMarkdown)
    .map((block) => block.replace(/\s+/g, " ").trim())
    .filter(Boolean);

  const firstParagraph = paragraphs[0];
  if (!firstParagraph) {
    return null;
  }

  return firstParagraph.length > 120 ? `${firstParagraph.slice(0, 117)}...` : firstParagraph;
}

function estimateReadingTime(content) {
  const text = stripMarkdown(content);
  const wordCount = text
    .split(/\s+/)
    .map((word) => word.trim())
    .filter(Boolean).length;
  const minutes = Math.max(1, Math.ceil(wordCount / 220));

  return `${minutes} min read`;
}

function humanizeSlug(slug) {
  return slug
    .replace(/[-_]+/g, " ")
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

const posts = fs
  .readdirSync(contentDir)
  .filter((fileName) => fileName.endsWith(".md"))
  .map((fileName) => {
    const filePath = path.join(contentDir, fileName);
    const raw = fs.readFileSync(filePath, "utf8");
    const { data, content } = matter(raw);
    const body = content.trim();
    const slug = path.basename(fileName, ".md");
    const title = toNonEmptyString(data.title) ?? getMarkdownTitle(body) ?? humanizeSlug(slug);
    const excerpt =
      toNonEmptyString(data.excerpt) ?? getExcerptFromContent(body) ?? `${title} 的内容摘要。`;
    const description = toNonEmptyString(data.description) ?? excerpt;
    const hero = data.hero && typeof data.hero === "object" ? data.hero : {};

    return {
      slug,
      title,
      excerpt,
      description,
      publishedAt: resolvePublishedAt(data.publishedAt),
      readingTime: toNonEmptyString(data.readingTime) ?? estimateReadingTime(body),
      category: toNonEmptyString(data.category) ?? "Notes",
      eyebrow:
        toNonEmptyString(data.eyebrow) ??
        `Essay ${resolvePublishedAt(data.publishedAt).slice(5).replace("-", ".")}`,
      featured: Boolean(data.featured),
      tags: Array.isArray(data.tags) ? data.tags.map((tag) => String(tag)) : [],
      accent: toNonEmptyString(data.accent) ?? defaultAccent,
      hero: {
        kicker: toNonEmptyString(hero.kicker) ?? excerpt,
        summary: toNonEmptyString(hero.summary) ?? description,
      },
      content: body,
    };
  })
  .sort((a, b) => b.publishedAt.localeCompare(a.publishedAt));

fs.mkdirSync(outputDir, { recursive: true });

const fileContent = `/* eslint-disable */
// This file is auto-generated by scripts/generate-blog-manifest.mjs

export const blogPosts = ${JSON.stringify(posts, null, 2)} as const;
`;

fs.writeFileSync(outputFile, fileContent);

console.log(`Generated ${outputFile}`);
