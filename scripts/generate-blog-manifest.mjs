import fs from "node:fs";
import path from "node:path";

import matter from "gray-matter";

const rootDir = process.cwd();
const contentDir = path.join(rootDir, "content", "blog");
const outputDir = path.join(rootDir, "lib", "generated");
const outputFile = path.join(outputDir, "blog.generated.ts");
const routeOutputDir = path.join(rootDir, "app", "blog", "(posts)");

function getCurrentDateString() {
  return new Date().toISOString().slice(0, 10);
}

function resolvePublishedAt(value) {
  if (value instanceof Date) {
    return value.toISOString().slice(0, 10);
  }

  if (typeof value === "string") {
    const normalized = value.trim().toLowerCase();

    if (normalized === "today" || normalized === "now" || normalized === "current") {
      return getCurrentDateString();
    }

    if (normalized) {
      return value;
    }
  }

  return getCurrentDateString();
}

function isValidFrontmatter(data) {
  if (!data || typeof data !== "object") {
    return false;
  }

  return [
    "title",
    "excerpt",
    "description",
    "readingTime",
    "category",
    "eyebrow",
    "accent",
  ].every((key) => data[key] != null);
}

const posts = fs
  .readdirSync(contentDir)
  .filter((fileName) => fileName.endsWith(".md"))
  .map((fileName) => {
    const filePath = path.join(contentDir, fileName);
    const raw = fs.readFileSync(filePath, "utf8");
    const { data, content } = matter(raw);

    if (!isValidFrontmatter(data)) {
      return null;
    }

    const hero =
      data.hero && typeof data.hero === "object"
        ? data.hero
        : {};

    return {
      slug: path.basename(fileName, ".md"),
      title: String(data.title),
      excerpt: String(data.excerpt),
      description: String(data.description),
      publishedAt: resolvePublishedAt(data.publishedAt),
      readingTime: String(data.readingTime),
      category: String(data.category),
      eyebrow: String(data.eyebrow),
      featured: Boolean(data.featured),
      tags: Array.isArray(data.tags) ? data.tags.map((tag) => String(tag)) : [],
      accent: String(data.accent),
      hero: {
        kicker: String(hero.kicker ?? ""),
        summary: String(hero.summary ?? ""),
      },
      content: content.trim(),
    };
  })
  .filter(Boolean)
  .sort((a, b) => b.publishedAt.localeCompare(a.publishedAt));

fs.mkdirSync(outputDir, { recursive: true });
fs.rmSync(routeOutputDir, { recursive: true, force: true });
fs.mkdirSync(routeOutputDir, { recursive: true });

const fileContent = `/* eslint-disable */
// This file is auto-generated by scripts/generate-blog-manifest.mjs

export const blogPosts = ${JSON.stringify(posts, null, 2)} as const;
`;

fs.writeFileSync(outputFile, fileContent);

for (const post of posts) {
  const postDir = path.join(routeOutputDir, post.slug);
  const routeFile = path.join(postDir, "page.tsx");

  fs.mkdirSync(postDir, { recursive: true });
  fs.writeFileSync(
    routeFile,
    `import { BlogPostTemplate, getBlogPostMetadata } from "@/app/blog/post-page-template";

export const dynamic = "force-static";

export async function generateMetadata() {
  return getBlogPostMetadata(${JSON.stringify(post.slug)});
}

export default function Page() {
  return <BlogPostTemplate slug=${JSON.stringify(post.slug)} />;
}
`,
  );
}

console.log(`Generated ${outputFile}`);
console.log(`Generated static blog routes in ${routeOutputDir}`);
